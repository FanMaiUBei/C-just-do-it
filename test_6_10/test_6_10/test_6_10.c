//理解浮点数的存储规则，
int main()
{
	int n = 9;//00000000000000000000000000001001
	float* pFloat = (float*)&n;
	printf("n的值为：%d\n", n);//%d打印整形，及打印9
	printf("pFloat的值为:%f\n", *pFloat);//将*pFloat该地址00000000000000000000000000001001当成浮点数打印，其中E为全0，及*pFloat=（-1)^0*00000000000000000001001*2^-127=0
	*pFloat = 9.0;//1001
	//(-1)^0*1.001*2^3;
	//0 10000001 00100000000000000000000
	printf("num的值为：%d\n", n);//打印整形，及将0 10000001 00100000000000000000000转化为10进制打印，为1091567616
	printf("pFloat的值为:%f\n", *pFloat);//9.000000
	return 0;
}